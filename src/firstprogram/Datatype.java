package firstprogram;

public class Datatype {

	public static void main(String[] args) {
    
    byte myByte =127;
    System.out.println("Bytevalue is:"+myByte);
    
    short myshort =32767;
    System.out.println("shortvalue is:"+myshort);
    
    int myint =2147483647;
    System.out.println("intvale is:"+myint);
    
    long myvalue = 9223372036854775807L;
    System.out.println("longvalue is:"+myvalue);
    
    // for decimal values
   
    float myfloat = 32.56123421F;
    System.out.println("my float value is:"+myfloat);
    
    double mydouble = 32.56123421;
    System.out.println("my double value is:"+mydouble);
    
    // for single characters
    
    char mygender = 'M';
    System.out.println(mygender);
    
    // for true/flase
    
    boolean mystatus =true;
    System.out.println(mystatus);
    
   
    
    
	}

}
